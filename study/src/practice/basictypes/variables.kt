package practice.basictypes

fun main() {
    val a: Int = 1000
    val b = "log message"
    val c = 3.14
    val d = 100_000_000_000_000
    val e = false
    val f = '\n'

    println(a::class)
    println(b::class)
    println(c::class)
    println(d::class)
    println(e::class)
    println(f::class)
}